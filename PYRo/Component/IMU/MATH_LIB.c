float quick_sqrt(float x) 
{
	float halfx = 0.5f * x;
	float y = x;
	long i = *(long* )&y;
	i = 0x5f3759df - (i >> 1);
	y = *(float* )&i;
	y = y * (1.5f - (halfx * y * y));
	return y;
}

//斜波函数初始化
void ramp_init(ramp_t *ramp_source_type, float frame_period, float max, float min)
{
    ramp_source_type->dt = frame_period;
    ramp_source_type->max_value = max;
    ramp_source_type->min_value = min;
    ramp_source_type->slope = 0.0f;
}

void ramp_calc(ramp_t *ramp, float32_t slope)
{
	ramp->slope = slope;
    ramp->out += ramp->slope * ramp->dt;
    if (ramp->out > ramp->max_value)
    {
        ramp->out = ramp->max_value;
    }
    else if (ramp->out < ramp->min_value)
    {
        ramp->out = ramp->min_value;
    }
}

float32_t float32_abs(float32_t input)
{
    if (input > 0)
    {
        return input;
    }
    else
    {
        return -input;
    }
}

float32_t float32_limit(float32_t input, float32_t min, float32_t max)
{
    if (input < min)
        return min;
    else if (input > max)
        return max;
    else
        return input;
}

float32_t rad_format(float32_t input)
{
    if (input > PI)
    {
        while (input > PI)
        {
            input -= 2 * PI;
        }
    }
    else if (input < -PI)
    {
        while (input < -PI)
        {
            input += 2 * PI;
        }
    }
    return input;
}

